package week_2_control_flow.day_3_for_loop;

public class ForLoopV1 {
    public static void main(String[] args) {

        // for (start; condition; post-increment)
        System.out.println("Post-increment");
        for (int i = 1; i <= 5; i++) {
            System.out.println("Count: " + i);
        }
        // Output: 1, 2, 3, 4, 5
    }
}
