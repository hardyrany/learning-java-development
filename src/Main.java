
public class Main {
    public static void main(String[] args) {
        
        // printf is used for formatted output (like print with formatting options)
        System.out.printf("Hello and welcome!\n");

        // loop runs from 1 to 5 (inclusive)
        for (int i = 1; i <= 5; i++) {
            
            // prints current value of i in each iteration
            System.out.println("i = " + i);
        }
    }
}